package chapter_one;

/**
 * 1.9 (Area and perimeter of a rectangle) Write a program that displays the area and
 * perimeter of a rectangle with a width of 5.3 and height of 8.6 using the following formula:
 * area = width * height
 * perimeter = 2 * (width + height)
 *
 * @author Sharaf Qeshta */

public class Problem$09
{
    public static void main(String[] args)
    {
        System.out.println(5.3 * 8.6);
        System.out.println(2 * (5.3 + 8.6));
    }
}
